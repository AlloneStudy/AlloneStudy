package redis.redisJson.domain.goods;

public class GoodInfo {
	public String bp_id;
	public String from_product;
	public String name;
	public String cover;
	public String pic;
	public String big;
	public String content;
	public String from_brand;
	public String price;
	
	/**
	 * @return the bp_id
	 */
	public String getBp_id() {
		return bp_id;
	}
	/**
	 * @param bp_id the bp_id to set
	 */
	public void setBp_id(String bp_id) {
		this.bp_id = bp_id;
	}
	/**
	 * @return the from_product
	 */
	public String getFrom_product() {
		return from_product;
	}
	/**
	 * @param from_product the from_product to set
	 */
	public void setFrom_product(String from_product) {
		this.from_product = from_product;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the cover
	 */
	public String getCover() {
		return cover;
	}
	/**
	 * @param cover the cover to set
	 */
	public void setCover(String cover) {
		this.cover = cover;
	}
	/**
	 * @return the pic
	 */
	public String getPic() {
		return pic;
	}
	/**
	 * @param pic the pic to set
	 */
	public void setPic(String pic) {
		this.pic = pic;
	}
	/**
	 * @return the big
	 */
	public String getBig() {
		return big;
	}
	/**
	 * @param big the big to set
	 */
	public void setBig(String big) {
		this.big = big;
	}
	/**
	 * @return the content
	 */
	public String getContent() {
		return content;
	}
	/**
	 * @param content the content to set
	 */
	public void setContent(String content) {
		this.content = content;
	}
	/**
	 * @return the from_brand
	 */
	public String getFrom_brand() {
		return from_brand;
	}
	/**
	 * @param from_brand the from_brand to set
	 */
	public void setFrom_brand(String from_brand) {
		this.from_brand = from_brand;
	}
	/**
	 * @return the price
	 */
	public String getPrice() {
		return price;
	}
	/**
	 * @param price the price to set
	 */
	public void setPrice(String price) {
		this.price = price;
	}
	
}